package introduction_to_java.first_program;

public class PrimetiveDatatypes {
	public static void main(String[] args) {
//primetive datatypes
		/*
		 * byte = 23 (default = 0)
		 * short = 300 (default = 0)
		 * int = 3000 (default = 0)
		 * long = 300L (default = 0)
		 * float = 8.9f (default = 0)
		 * double = 9.1 (default = 0)
		 * char = 'a' (default = 0)
		 * boolean = true (default = 0)
		 */ 
		
		/* Declaration variable
		 * variable it is a container for value
		 * 
		 * 
		 */
		byte age;
		age = 20;
//datatype variable name = value;
		
		byte age1 = 30;
		System.out.println(age);
		System.out.println(age1);
		
		short len = 300;
		System.out.println(len);
		
		int weight = 50;
		System.out.println(weight);
		
		long phone = 9885885L;
		System.out.println(phone);
		
		float value = 88.8f;
		System.out.println(value);
		
		double num = 7;
		System.out.println(num);
		
		char name = 'a';
		System.out.println(name);
		
		boolean res = true;
		System.out.println(res);
		
		
		
		
		
	}

}
